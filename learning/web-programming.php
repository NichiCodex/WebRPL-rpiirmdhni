						<h4 class="fw-bolder mb-3">What are the Programming Languages used in Web Programming?</h4>
						<ol type="1" class="mb-0">
                            <li>
                                <strong>HTML (HyperText Markup Language)</strong><br>
                                HTML is a markup language used to define the structure and content of web pages. It organizes elements such as text, images, links, tables, and forms.
                            </li>
                            <li>
                                <strong>CSS (Cascading Style Sheets)</strong><br>
                                CSS is a language used to control the visual presentation of web pages. It covers aspects like colors, layout, font sizes, and responsiveness for various devices.
                            </li>
                            <li>
                                <strong>JavaScript</strong><br>
                                JavaScript is a programming language commonly used to create dynamic interactions and functionality on web pages. With JavaScript, you can control elements, respond to user actions, and communicate with servers.
                            </li>
                            <li>
                                <strong>PHP</strong><br>
                                PHP is a server-side programming language widely used to generate dynamic content in web pages. It often interacts with databases and provides business logic on the server side.
